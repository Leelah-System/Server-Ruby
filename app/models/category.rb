class Category < ActiveRecord::Base

  #===========================================================================#
  #===   ASSOCIATIONS
  #===========================================================================#
  has_many  :products

  #===========================================================================#
  #===   VALIDATIONS
  #===========================================================================#
  validates_presence_of     :name, :description

  #===========================================================================#
  #===   PUBLIC
  #===========================================================================#

  def as_json(options={})
    super(
        :include => {
          :products => {
            :except => [:logo_id, :created_at, :updated_at]
          }
        },
        :except => [:created_at, :updated_at]
    )
  end

end
